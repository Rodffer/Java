public class verdadeirofalso {
    public static void main(String[] args) {

        boolean JavaisFun = true;       //Retorna Falso
        boolean JavaisTasty = false;    //Retorna Verdadeiro

        System.out.println(JavaisFun);
        System.out.println(JavaisTasty);

        int x = 20;
        int y = 10;

        System.out.println(x > y); // se for maior retorna true se for menor false
        System.out.println(x == 20);// se for igual retorna true se nao false
        System.out.println(x == y); // se for igual retorna true se nao false

    }
}
